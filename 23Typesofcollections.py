# A collection is a single variable used to store multiple values.
    # List = [] Is ordered and changeable. Duplicates OK
    # Set = {} Is unordered and immutable, but add/remove OK. No duplicates.
    # Tuple = () Is ordered and uncheangable, duplicates OK. Faster. 

# To store more than one value in a variable:
fruits = ["apple", "watermelon", "lemon", "banana", "mango"]
print(fruits[0])        # This will print "apple" because the value has the 0 value in the string
print(fruits[3])        # This will print "banana" because the value has the 3 value in the string
print(fruits[:3:])
print(fruits[::-1])     # To print it backwards