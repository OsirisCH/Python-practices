# Typecasting: The process of converting a variable from one data type to other. It uses functions.
# Useful for user inputs that are usualy strings
name_dog = "Lisa"
age = 8
food_price = 5.70
has_owner = True

# Function str()
age = str(age)
print (age)
print (type(age))

# Function int()
food_price = int(food_price)
print (food_price)
print (type(food_price))

# Function float()
age = float(age)
print (age)
print (type(age))

# Function bool() 
name = bool(name_dog)
print (name_dog)
